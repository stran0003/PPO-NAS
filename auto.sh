#!/bin/bash
# ============================================================================
# PPO-NAS 一键运行脚本
# ============================================================================
# 用法:
#   bash auto.sh                  # 默认配置运行
#   bash auto.sh --mock           # 快速测试（不训练真实模型）
#   bash auto.sh --resume 100     # 从第100轮checkpoint恢复
#   bash auto.sh -n 50            # 只跑50轮
# ============================================================================

set -e

# -- 项目目录 --
PROJECT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "${PROJECT_DIR}"

# -- 创建输出目录 --
mkdir -p logs checkpoints output/plots

echo "============================================"
echo "PPO-NAS 一键运行"
echo "============================================"
echo "项目目录: ${PROJECT_DIR}"
echo "时间:     $(date '+%Y-%m-%d %H:%M:%S')"
echo "--------------------------------------------"

# -- 运行 --
python main.py --config config.yaml "$@"
